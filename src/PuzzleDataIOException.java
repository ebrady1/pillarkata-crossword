public class PuzzleDataIOException extends PuzzleDataException
{
    public PuzzleDataIOException()
    {
    }

    public PuzzleDataIOException(String message)
    {
        super(message);
    }

    public PuzzleDataIOException(String message, Throwable cause)
    {
        super(message, cause);
    }

    public PuzzleDataIOException(Throwable cause)
    {
        super(cause);
    }

    public PuzzleDataIOException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace)
    {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
