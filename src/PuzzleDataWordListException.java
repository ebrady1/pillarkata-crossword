public class PuzzleDataWordListException extends PuzzleDataException
{
	public PuzzleDataWordListException()
	{
	}

	public PuzzleDataWordListException(String message)
	{
		super(message);
	}

	public PuzzleDataWordListException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public PuzzleDataWordListException(Throwable cause)
	{
		super(cause);
	}

	public PuzzleDataWordListException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace)
	{
		super(message, cause, enableSuppression, writableStackTrace);
	}
}
