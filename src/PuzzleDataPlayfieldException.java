public class PuzzleDataPlayfieldException extends PuzzleDataException
{
	public PuzzleDataPlayfieldException()
	{
	}

	public PuzzleDataPlayfieldException(String message)
	{
		super(message);
	}

	public PuzzleDataPlayfieldException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public PuzzleDataPlayfieldException(Throwable cause)
	{
		super(cause);
	}

	public PuzzleDataPlayfieldException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace)
	{
		super(message, cause, enableSuppression, writableStackTrace);
	}
}
